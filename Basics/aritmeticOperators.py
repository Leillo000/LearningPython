# For sums
2 + 2

# Multiplication
5 * 6

# With parenthesis
(50 - 5 * 6) / 4

# For divisions
8 / 5  
# division always returns a floating-point number

# Returns the remainder of this division
17 % 3
# R: 2

# Returns just the integer of the result, skips the fractional part
17 // 3
#R: 5, without the extra "/", the result is 5.666666667

5 ** 2 
# 5 squared

2 ** 7  
# 2 to the power of 7